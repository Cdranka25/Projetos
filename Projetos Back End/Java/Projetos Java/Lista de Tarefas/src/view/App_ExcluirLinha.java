    private void jB_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jB_ExcluirActionPerformed
        String linhaStr = jTextField1.getText().trim();
        if (linhaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Informe o número da linha.", "Atenção", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            int numeroLinha = Integer.parseInt(linhaStr);
            // Chama o método de exclusão na tela principal (App)
            app.excluirLinha(numeroLinha);
            JOptionPane.showMessageDialog(this, "Linha " + numeroLinha + " excluída com sucesso!");
            jTextField1.setText("");
            app.recalcular();
            this.dispose();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Digite um número inteiro válido.", "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }// GEN-LAST:event_jB_ExcluirActionPerformed

